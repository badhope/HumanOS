export const questionDimensions: Record<string, string> = STANDARDIZED_QUESTIONS.reduce(
  (acc, q) => ({ ...acc, [q.id]: q.dimension }),
  {}
)

export function createQuestionSet(mode: 'normal' | 'advanced' | 'professional'): StandardizedQuestion[] {
  const questionsPerDimension = {
    normal: 12,
    advanced: 9,
    professional: 15,
  }

  const dimensions = ['economic', 'social', 'cultural', 'international', 'ecological']
  const result: StandardizedQuestion[] = []

  dimensions.forEach((dim) => {
    const dimQuestions = STANDARDIZED_QUESTIONS.filter(q => q.dimension === dim)
    const shuffled = [...dimQuestions].sort(() => Math.random() - 0.5)
    result.push(...shuffled.slice(0, questionsPerDimension[mode]))
  })

  return result.sort(() => Math.random() - 0.5)
}
